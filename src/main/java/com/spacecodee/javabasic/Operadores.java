package com.spacecodee.javabasic;

public class Operadores {
    /*
     * Suma => + | Concatenar | ++
     * Resta => - | --
     * Multiplicacion => *
     * Division => /
     * Modulo => %
     * */

    int anInt = 0;

    public void aument() {
        this.anInt++; // 0 => 1
        ++this.anInt; // 1 => 2
    }

}
